public class BMI {
    public static void main(String[] args) {
        double weight = 77;
        double height = 1.76;
        double BMI;
        BMI = weight/ (height * height);
        System.out.println("BMI for weight " + weight + "kg");
        System.out.println("and height " + height + "m");
        System.out.println("is equivalent to " + BMI + ".");

    }
}